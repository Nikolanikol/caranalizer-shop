export function getFuelSurcharge(destination: EmsCountryCode, weightKg: number): number {
  const perKg = EMS_FUEL_SURCHARGE_PER_KG[destination] ?? 0;
  return Math.ceil(weightKg) * perKg;
}

export function getVolumetricWeight(lengthCm: number, widthCm: number, heightCm: number): number {
  return (lengthCm * widthCm * heightCm) / 6000;
}

export function getBillableWeight(actualKg: number, lengthCm: number, widthCm: number, heightCm: number): number {
  return Math.max(actualKg, getVolumetricWeight(lengthCm, widthCm, heightCm));
}

export function getEmsTotalCost(destination: EmsCountryCode, weightKg: number): number | null {
  const base = getEmsRateForWeight(destination, weightKg);
  if (base === null) return null;
  return base + getFuelSurcharge(destination, weightKg);
}

export function getEmsRate(destination: EmsCountryCode | EmsZone, weightKg: number): number | null {
  const rates = EMS_RATES[destination];
  if (!rates) return null;
  const idx = EMS_WEIGHTS.indexOf(weightKg as typeof EMS_WEIGHTS[number]);
  if (idx === -1) return null;
  return rates[idx];
}

export function getEmsRateForWeight(destination: EmsCountryCode | EmsZone, weightKg: number): number | null {
  const rates = EMS_RATES[destination];
  if (!rates) return null;
  if (weightKg <= 0 || weightKg > 30) return null;
  for (let i = 0; i < EMS_WEIGHTS.length; i++) {
    if (weightKg <= EMS_WEIGHTS[i]) {
      return rates[i];
    }
  }
  return rates[rates.length - 1];
}
